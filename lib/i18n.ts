// Pluralization helper
const pluralize = (lang: Language, count: number, forms: string[]): string => {
  if (forms.length === 0) return ""

  if (lang === "ru") {
    if (forms.length === 3) {
      const cases = [2, 0, 1, 1, 1, 2]
      const formIndex =
        count % 100 > 4 && count % 100 < 20 ? 2 : cases[Math.min(count % 10, 5)]
      return forms[formIndex]
    }
  }

  if (forms.length >= 2) {
    return count === 1 ? forms[0] : forms[1]
  }

  return forms[0] // fallback
}

export const useTranslation = (language: Language) => {
  const t = (
    key: keyof typeof translations.en,
    params?: Record<string, string | number>
  ): string => {
    let str = translations[language]?.[key] ?? translations.en[key] ?? key
    const count = params?.count as number

    if (count !== undefined && (key === "guestForms" || key === "nights")) {
      const forms = str.split(" | ")
      const pluralizedWord = pluralize(language, count, forms)
      str = `${count} ${pluralizedWord}` // Исправлено: добавляем число перед словом
    }

    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        str = str.replace(`{${k}}`, String(v))
      })
    }

    return str
  }

  return { t }
}
